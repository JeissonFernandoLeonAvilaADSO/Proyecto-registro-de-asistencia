    private void TiposDocsBtnItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_TiposDocsBtnItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            // El botón está activo, cambia el color de fondo a verde
            TiposDocsBtn.setBackground(Color.decode("#39A900"));
            TiposDocsBtn.setForeground(Color.WHITE);
            
            for (Component comp : PanAct.getComponents()){
                comp.setEnabled(true);
            }
            
            // Restablecer el fondo de los otros botones
            GenBtn.setSelected(false);
            GenBtn.setBackground(Color.WHITE);
            GenBtn.setForeground(Color.BLACK);

            FichasBtn.setSelected(false);
            FichasBtn.setBackground(Color.WHITE);
            FichasBtn.setForeground(Color.BLACK);

            PrgmFormBtn.setSelected(false);
            PrgmFormBtn.setBackground(Color.WHITE);
            PrgmFormBtn.setForeground(Color.BLACK);

            AmbBtn.setSelected(false);
            AmbBtn.setBackground(Color.WHITE);
            AmbBtn.setForeground(Color.BLACK);
        } else {
            for (Component comp : PanAct.getComponents()){
            comp.setEnabled(false);
            }
        }
    }//GEN-LAST:event_TiposDocsBtnItemStateChanged

